"""검증용 더미 대장 엑셀(dummy_ledger.xlsx)을 생성한다."""

from pathlib import Path

import pandas as pd

OUTPUT_PATH = Path(__file__).resolve().parent / "dummy_ledger.xlsx"

HEADERS = [
    "연번",
    "이름",
    "성별",
    "주소",
    "연락처",
    "차량번호",
    "차종",
    "구분",
    "등록일",
    "등록기간",
    "비고",
]


def main() -> None:
    rows: list[dict] = []

    # 정상 12행
    genders = ["남", "여", "기타"]
    categories = ["거주자", "회사원", "기타"]
    for i in range(1, 13):
        rows.append(
            {
                "연번": i,
                "이름": f"엑셀테스트{i}",
                "성별": genders[(i - 1) % 3],
                "주소": f"서울시 테스트구 이관로 {i}",
                "연락처": f"010-1000-{i:04d}",
                "차량번호": f"34나{i:04d}",
                "차종": f"이관모델{i}",
                "구분": categories[(i - 1) % 3],
                "등록일": "2025-03-01",
                "등록기간": "2025-01-01 ~ 2026-12-31",
                "비고": "정상 이관 대상",
            }
        )

    # 차량번호 빈 행 2건 (건너뛰기 검증)
    rows.append(
        {
            "연번": 13,
            "이름": "엑셀테스트빈차1",
            "성별": "남",
            "주소": "서울시 테스트구 이관로 13",
            "연락처": "010-1000-0013",
            "차량번호": "",
            "차종": "이관모델13",
            "구분": "기타",
            "등록일": "2025-03-01",
            "등록기간": "2025-01-01 ~ 2026-12-31",
            "비고": "차량번호 누락",
        }
    )
    rows.append(
        {
            "연번": 14,
            "이름": "엑셀테스트빈차2",
            "성별": "여",
            "주소": "서울시 테스트구 이관로 14",
            "연락처": "010-1000-0014",
            "차량번호": None,
            "차종": "이관모델14",
            "구분": "거주자",
            "등록일": "2025-03-01",
            "등록기간": "2025-01-01 ~ 2026-12-31",
            "비고": "차량번호 누락",
        }
    )

    # 등록기간 비정상 1건 (경고 검증용 — 차량번호는 있어 이관됨)
    rows.append(
        {
            "연번": 15,
            "이름": "엑셀테스트기간깨짐",
            "성별": "기타",
            "주소": "서울시 테스트구 이관로 15",
            "연락처": "010-1000-0015",
            "차량번호": "34나0015",
            "차종": "이관모델15",
            "구분": "회사원",
            "등록일": "2025-03-01",
            "등록기간": "기간형식아님",
            "비고": "등록기간 파싱 실패 예상",
        }
    )

    df = pd.DataFrame(rows, columns=HEADERS)
    df.to_excel(OUTPUT_PATH, index=False, engine="openpyxl")
    print(f"더미 엑셀 생성 완료: {OUTPUT_PATH}")
    print(f"  총 {len(df)}행 (정상 12 + 차량번호 빈 2 + 기간 파싱 실패 1)")


if __name__ == "__main__":
    main()
